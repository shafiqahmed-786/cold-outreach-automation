"""
Brevo (formerly Sendinblue) API wrapper – Stage 4.

Sends one transactional email per verified contact via Brevo's
POST /v3/smtp/email endpoint.

Endpoint (confirmed from public Brevo docs):
    POST https://api.brevo.com/v3/smtp/email
    Headers: { "api-key": "<BREVO_API_KEY>" }
    Body:
    {
        "sender":     { "name": "...", "email": "..." },
        "to":         [{ "email": "...", "name": "..." }],
        "subject":    "...",
        "htmlContent": "...",
        "textContent": "..."
    }

Design decisions:
- One API call per contact (Brevo's transactional endpoint is per-message).
- Fan-out is bounded by CONCURRENT_REQUESTS semaphore to avoid 429s.
- A failure on one send does NOT abort the rest; failures are collected and
  returned in BrevoResult.emails_failed for post-run inspection.
- Both htmlContent and textContent are sent so mail clients without HTML
  rendering still get a readable message.
- Email body is generated by a pure function `_build_email_body` – easy to
  unit-test and swap for a Jinja2 template later without touching the HTTP layer.
"""

