# ---------------------------------------------------------------------------
# Service class
# ---------------------------------------------------------------------------

class BrevoService(BaseAPIClient):
    base_url = cfg.BREVO_BASE_URL

    def _headers(self) -> dict:
        return {
            "api-key": cfg.BREVO_API_KEY,  # Brevo uses 'api-key' header, NOT Bearer.
            "Content-Type": "application/json",
            "Accept": "application/json",
        }

    @async_retry(
        max_attempts=cfg.MAX_RETRIES,
        base_delay=cfg.RETRY_BASE_DELAY,
        exceptions=(aiohttp.ClientError, aiohttp.ServerConnectionError, asyncio.TimeoutError),
    )
    async def _send_single(self, contact: VerifiedContact) -> dict:
        """Make one Brevo transactional send request."""
        subject, html_body, text_body = _build_email_body(contact)
        payload = {
            "sender": {
                "name": cfg.BREVO_SENDER_NAME,
                "email": cfg.BREVO_SENDER_EMAIL,
            },
            "to": [{"email": contact.email, "name": contact.full_name}],
            "subject": subject,
            "htmlContent": html_body,
            "textContent": text_body,
        }
        return await self._post("/smtp/email", headers=self._headers(), payload=payload)

    async def _send_one(
        self, contact: VerifiedContact
    ) -> tuple[SentEmail | None, dict | None]:
        """
        Send to one contact; return (SentEmail, None) on success or
        (None, failure_dict) on error — never raises.
        """
        try:
            raw = await self._send_single(contact)
            message_id = raw.get("messageId") or raw.get("message_id")
            sent = SentEmail(
                recipient_email=contact.email,
                recipient_name=contact.full_name,
                company_name=contact.company_name,
                message_id=str(message_id) if message_id else None,
                status="sent",
            )
            logger.info(
                "[Stage 4] Email sent → %s (%s) | messageId=%s",
                contact.email,
                contact.full_name,
                message_id,
            )
            return sent, None
        except Exception as exc:
            logger.error(
                "[Stage 4] Failed to send to %s (%s): %s",
                contact.email,
                contact.full_name,
                exc,
            )
            return None, {
                "email": contact.email,
                "name": contact.full_name,
                "error": str(exc),
            }

    # ------------------------------------------------------------------
    # Public interface
    # ------------------------------------------------------------------

    async def send_emails(self, contacts: list[VerifiedContact]) -> BrevoResult:
        """
        Fan-out email sends across all verified contacts.
        Bounded by CONCURRENT_REQUESTS semaphore.
        Partial failures are collected; they do NOT abort the run.
        """
        logger.info("[Stage 4] Sending emails to %d contacts.", len(contacts))
        sem = asyncio.Semaphore(cfg.CONCURRENT_REQUESTS)

        async def bounded(contact: VerifiedContact):
            async with sem:
                return await self._send_one(contact)

        outcomes = await asyncio.gather(*[bounded(c) for c in contacts])

        sent: list[SentEmail] = []
        failed: list[dict] = []
        for ok, err in outcomes:
            if ok:
                sent.append(ok)
            if err:
                failed.append(err)

        logger.info(
            "[Stage 4] Done. Sent=%d, Failed=%d.", len(sent), len(failed)
        )
        return BrevoResult(emails_sent=sent, emails_failed=failed)

    async def send_emails_mock(self, contacts: list[VerifiedContact]) -> BrevoResult:
        """Mock send – logs what would have been sent without hitting Brevo."""
        logger.info("[Stage 4][MOCK] Simulating email sends for %d contacts.", len(contacts))
        sent: list[SentEmail] = []
        for contact in contacts:
            subject, _, _ = _build_email_body(contact)
            logger.info(
                "[Stage 4][MOCK] Would send to %s | subject: %s",
                contact.email,
                subject,
            )
            sent.append(
                SentEmail(
                    recipient_email=contact.email,
                    recipient_name=contact.full_name,
                    company_name=contact.company_name,
                    message_id=f"mock-{contact.email}",
                    status="mock_sent",
                )
            )
        return BrevoResult(emails_sent=sent, emails_failed=[])